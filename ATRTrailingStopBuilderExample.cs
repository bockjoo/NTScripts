namespace NinjaTrader.NinjaScript.Strategies
{
	public class ATRTrailingStopBuilderExample : Strategy
	{
		/* SmoothMARibbon */
    	private EMA fastEMA;
    	private EMA slowEMA;

    	[NinjaScriptProperty]
    	[Range(1, int.MaxValue)]
    	[Display(Name = "Fast EMA Period", Order = 1, GroupName = "Parameters")]
    	public int FastPeriod { get; set; }

    	[NinjaScriptProperty]
    	[Range(1, int.MaxValue)]
    	[Display(Name = "Slow EMA Period", Order = 2, GroupName = "Parameters")]
    	public int SlowPeriod { get; set; }
		/* SmoothMARibbon */
		private int ReverseOffset;
		private double StopPrice;

		private ATRTrailing ATRTrailing1;
		
		private Series<double> MathSeries;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"";
				Name										= "ATRTrailingStopBuilderExample";
				Calculate									= Calculate.OnPriceChange;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.ImmediatelySubmitSynchronizeAccount;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 1;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				ATROffset					= 0;
				ReverseOffset					= 0;
				StopPrice					= 0;
				/* SmoothMARibbon */
	            FastPeriod = 10;
    	        SlowPeriod = 50;
	    	    //AddPlot(Color.Goldenrod, "fastEMA");
    	    	//AddPlot(Color.LightSalmon, "slowEMA");	
				AddPlot(Brushes.Blue, "fastEMA");
				AddPlot(Brushes.Red, "slowEMA");
				/* SmoothMARibbon */
			}
			else if (State == State.Configure)
			{
				/* SmoothMARibbon */
	            fastEMA = EMA(FastPeriod);
    	        slowEMA = EMA(SlowPeriod);
				/* SmoothMARibbon */
			}
			else if (State == State.DataLoaded)
			{				
				MathSeries = new Series<double>(this);			
				ATRTrailing1				= ATRTrailing(Close, 4, 10, 0.005);
				ATRTrailing1.Plots[0].Brush = Brushes.Blue;
				ATRTrailing1.Plots[1].Brush = Brushes.Red;
				AddChartIndicator(ATRTrailing1);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;
			
			/* SmoothMARibbon */
	        if (CurrentBar < Math.Max(FastPeriod, SlowPeriod))
    	        return;
			/* SmoothMARibbon */

			 // Set 1
			if ((Position.MarketPosition == MarketPosition.Flat)
				 && (ATRTrailing1.Lower[0] > 0)
				 && (IsFirstTickOfBar == true))
			{
				if (CrossAbove(fastEMA, slowEMA, 1)) { 			/* SmoothMARibbon */
				EnterLong(Convert.ToInt32(DefaultQuantity), @"longEntry");
				MathSeries[0] = ATROffset;
				ReverseOffset = Convert.ToInt32((MathSeries[0] * -1) );
				} 			/* SmoothMARibbon */
			}
			
			 // Set 2
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (ATRTrailing1.Lower[0] > 0)
				 && ((ATRTrailing1.Lower[0] + (ReverseOffset * TickSize))  < GetCurrentBid(0)))
			{
				StopPrice = (ATRTrailing1.Lower[0] + (ReverseOffset * TickSize)) ;
			}
			
			 // Set 3
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (StopPrice < GetCurrentBid(0)))
			{
				ExitLongStopMarket(Convert.ToInt32(DefaultQuantity), StopPrice, @"longAtrStopExit", @"longEntry");
			}
			
			 // Set 4
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (StopPrice >= GetCurrentBid(0)))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"longMarketExit", @"longEntry");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Range(-999, int.MaxValue)]
		[Display(Name="ATROffset", Description="Number of ticks to add/remove from ATRTrailing value for stop distance", Order=1, GroupName="Parameters")]
		public int ATROffset
		{ get; set; }
		#endregion

	}
}

